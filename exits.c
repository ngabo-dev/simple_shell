/**
 **_strncpy - duplicates a string
 *@dest: the string to be copied at destination to
 *@src: the initial string
 *@n: the amount of chars to be duplicated
 *Return: the string that has been joined
 */
char *_strncpy(char *dest, char *src, int n)
{
	int i, j;
	char *s = dest;

	i = 0;
	while (src[i] != '\0' && i < n - 1)
	{
		dest[i] = src[i];
		i++;
	}
	if (i < n)
	{
		j = i;
		while (j < n)
		{
			dest[j] = '\0';
			j++;
		}
	}
	return (s);
}

/**
 **_strncat - links two strings
 *@dest: the first string
 *@src: the second string
 *@n: the maximum number of bytes to be used
 *Return: the linked string
 */
char *_strncat(char *dest, char *src, int n)
{
	int i, j;
	char *s = dest;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0' && j < n)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	if (j < n)
		dest[i] = '\0';
	return (s);
}

/**
 **_strchr - seeks out a character within a string
 *@s: string that has to be parsed
 *@c: the char to look for
 *Return: A pointer (s) to the memory area s
 */
char *_strchr(char *s, char c)
{
	do {
		if (*s == c)
			return (s);
	} while (*s++ != '\0');

	return (NULL);
}

